# Using "in
Word = "apple"
print("a"in word)       # True -> because "a" is inside  "apple"
print ("z" in word)     # True -> because  "z" is not in "apple"

# Using "not in" 
print("z" not in word)  # True -> because "z" is NOT in "apple" 
print("a" not in word ) # True -> because " a" IS in "apple"